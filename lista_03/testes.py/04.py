"""
    append: adicona mais um elemento a lista (no final da lista)
"""

lista_alunos = ['Maria', 'Philip', 'Pietro', 'Edu', 'Marcos', 'Vicente']

print(lista_alunos)
lista_alunos.append('Manuel')
print(lista_alunos)

print(lista_alunos[1])
print(lista_alunos[2:])
print(lista_alunos[:4])
print(lista_alunos[1:6])
print(lista_alunos[-1])